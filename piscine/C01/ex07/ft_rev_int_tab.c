void	ft_rev_int_tab(int *tab, int size)
{
	int	q;
	int	tmp;

	q = 0;
	while (q < size / 2)
	{
		tmp = tab[q];
		tab[q] = tab[(size -1) - q];
		tab[(size - 1) - q] = tmp;
		q++;
	}
}
